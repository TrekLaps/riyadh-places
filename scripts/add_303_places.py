def gen_popular_times():
    days = ["saturday","sunday","monday","tuesday","wednesday","thursday","friday"]
    times = {}
    for d in days:
        hours = []
        for h in range(24):
            if h < 6:
                hours.append(random.randint(3, 15))
            elif h < 9:
                hours.append(random.randint(15, 45))
            elif h < 12:
                hours.append(random.randint(40, 85))
            elif h < 14:
                hours.append(random.randint(50, 90))
            elif h < 16:
                hours.append(random.randint(25, 55))
            elif h < 20:
                hours.append(random.randint(50, 95))
            elif h < 22:
                hours.append(random.randint(35, 70))
            else:
                hours.append(random.randint(10, 35))
        times[d] = hours
    return times

