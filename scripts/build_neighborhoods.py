def get_hood_data(key):
    info = ALL_HOODS[key]
    variants = info['variants']
    places = [p for p in data if p.get('neighborhood') in variants]
    cats = collections.Counter(p.get('category') for p in places)
    ratings = [p.get('google_rating', 0) for p in places]
    avg_rating = round(sum(ratings)/len(ratings), 1) if ratings else 0
    top_cat = cats.most_common(1)[0][0] if cats else 'N/A'
    n_cats = len(cats)
    return {
        'count': len(places),
        'avg_rating': avg_rating,
        'top_cat': top_cat,
        'n_cats': n_cats,
        'cats': cats,
    }

